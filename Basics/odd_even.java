package Lec_01;

public class odd_even {
    public static void main(String[] args) {
        int num = 991;
        int rem = num % 2;
        if (rem == 1) {
            System.out.println("it is a odd num");
        } else {
            System.out.println("it is a even num");

        }
        int num2 = 156;
        int rem2 = num2 % 2;
        if (rem2 == 1) {
            System.out.println("it is a odd num");
        } else {
            System.out.println("it is a even num");

        }

    }
}
