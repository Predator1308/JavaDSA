package Lec_01;

public class Assignment_Operators {

    public static void main(String[] args) {


        int x = 9;
        x += 6;// x= x+6;
        System.out.println(x);
        int p = 11;
        p -= 3;// p = p - 3;
        System.out.println(p);
        // p ki update value
        p *= 7;// p = p * 7;
        System.out.println(p);
        p /= 5;// p = p /5;
        System.out.println(p);

        p %= 3;// p = p % 3;
        System.out.println(p);
    }

}